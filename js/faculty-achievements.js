// Optimized Achievement Modal with performance improvements
function openAchievementModal(facultyName) {
  const faculty = facultyAchievements[facultyName];
  if (!faculty) return;

  const modal = document.getElementById('achievementModal');
  if (!modal) return;

  // Use DocumentFragment for better performance
  const fragment = document.createDocumentFragment();
  
  // Cache DOM elements
  const modalPhoto = document.getElementById('modalPhoto');
  const modalName = document.getElementById('modalName');
  const modalDesignation = document.getElementById('modalDesignation');
  const modalEmail = document.getElementById('modalEmail');
  const modalPhone = document.getElementById('modalPhone');
  const modalAchievements = document.getElementById('modalAchievements');

  // Set basic info with error handling
  if (modalPhoto) {
    modalPhoto.src = faculty.photo || '';
    modalPhoto.alt = faculty.name || '';
    modalPhoto.loading = 'lazy';
  }
  if (modalName) modalName.textContent = faculty.name || '';
  if (modalDesignation) modalDesignation.textContent = faculty.designation || '';
  if (modalEmail) {
    const gmailBuilder = window.buildGmailComposeUrl;
    const gmailUrl = typeof gmailBuilder === 'function'
      ? gmailBuilder(faculty.email)
      : `mailto:${faculty.email}`;
    modalEmail.innerHTML = `📧 <a href="${gmailUrl}" target="_blank" rel="noopener">${faculty.email}</a>`;
  }
  if (modalPhone) modalPhone.textContent = `📞 ${faculty.phone}`;
  
  // Add LinkedIn link if available (optimized)
  const modalInfo = document.querySelector('.achievement-modal__info');
  if (faculty.linkedin && modalInfo) {
    // Remove existing LinkedIn link efficiently
    const existingLinkedin = modalInfo.querySelector('.modal-linkedin');
    if (existingLinkedin) {
      existingLinkedin.remove();
    }
    
    const linkedinLink = document.createElement('a');
    linkedinLink.href = faculty.linkedin;
    linkedinLink.className = 'modal-linkedin';
    linkedinLink.target = '_blank';
    linkedinLink.rel = 'noopener noreferrer';
    linkedinLink.innerHTML = `
      <svg fill="currentColor" viewBox="0 0 24 24" xmlns="http://www.w3.org/2000/svg" aria-hidden="true">
        <path d="M19 0h-14c-2.761 0-5 2.239-5 5v14c0 2.761 2.239 5 5 5h14c2.762 0 5-2.239 5-5v-14c0-2.761-2.238-5-5-5zm-11 19h-3v-11h3v11zm-1.5-12.268c-.966 0-1.75-.79-1.75-1.764s.784-1.764 1.75-1.764 1.75.79 1.75 1.764-.783 1.764-1.75 1.764zm13.5 12.268h-3v-5.604c0-3.368-4-3.113-4 0v5.604h-3v-11h3v1.765c1.396-2.586 7-2.777 7 2.476v6.759z"/>
      </svg>
      View LinkedIn Profile
    `;
    
    modalInfo.appendChild(linkedinLink);
  }

  // Build achievements HTML efficiently
  if (modalAchievements && faculty.achievements) {
    const achievementSections = Object.entries(faculty.achievements).map(([category, items]) => {
      const section = document.createElement('div');
      section.className = 'achievement-section';
      
      const heading = document.createElement('h3');
      heading.textContent = category;
      section.appendChild(heading);
      
      const list = document.createElement('ul');
      items.forEach(item => {
        const listItem = document.createElement('li');
        listItem.textContent = item;
        list.appendChild(listItem);
      });
      section.appendChild(list);
      
      return section;
    });
    
    // Clear and append all sections at once
    modalAchievements.innerHTML = '';
    achievementSections.forEach(section => modalAchievements.appendChild(section));
  }

  // Show modal with animation frame for smooth transition
  requestAnimationFrame(() => {
    modal.classList.add('active');
    document.body.style.overflow = 'hidden';
    
    // Focus management for accessibility
    const closeButton = modal.querySelector('.achievement-modal__close');
    if (closeButton) {
      closeButton.focus();
    }
  });
}

// Optimized Close Achievement Modal
function closeAchievementModal() {
  const modal = document.getElementById('achievementModal');
  if (!modal) return;
  
  requestAnimationFrame(() => {
    modal.classList.remove('active');
    document.body.style.overflow = '';
    
    // Return focus to the clicked card for accessibility
    const activeCard = document.querySelector('.faculty-card:focus');
    if (activeCard) {
      activeCard.focus();
    }
  });
}

// Optimized event delegation and initialization
document.addEventListener('DOMContentLoaded', function() {
  // Use event delegation on document body for all faculty cards
  document.body.addEventListener('click', function(e) {
    const card = e.target.closest('.faculty-card');
    if (card && !e.target.closest('a')) {
      const facultyName = card.getAttribute('data-faculty');
      if (facultyName) {
        openAchievementModal(facultyName);
      }
    }
  });

  // Optimized modal event listeners
  const modal = document.getElementById('achievementModal');
  if (modal) {
    // Close modal when clicking backdrop
    modal.addEventListener('click', function(e) {
      if (e.target === modal) {
        closeAchievementModal();
      }
    });
    
    // Close button
    const closeButton = modal.querySelector('.achievement-modal__close');
    if (closeButton) {
      closeButton.addEventListener('click', closeAchievementModal);
    }
  }

  // Optimized keyboard event handling
  let isModalOpen = false;
  
  document.addEventListener('keydown', function(e) {
    const modal = document.getElementById('achievementModal');
    isModalOpen = modal && modal.classList.contains('active');
    
    if (e.key === 'Escape' && isModalOpen) {
      closeAchievementModal();
    }
  });
  
  // Performance: Pause animations when page is hidden
  document.addEventListener('visibilitychange', function() {
    if (document.hidden && isModalOpen) {
      // Optionally pause any modal animations when tab is hidden
      const modal = document.getElementById('achievementModal');
      if (modal) {
        modal.style.animationPlayState = 'paused';
      }
    } else if (!document.hidden && isModalOpen) {
      const modal = document.getElementById('achievementModal');
      if (modal) {
        modal.style.animationPlayState = 'running';
      }
    }
  });
});
